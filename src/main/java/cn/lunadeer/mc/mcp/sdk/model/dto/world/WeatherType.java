package cn.lunadeer.mc.mcp.sdk.model.dto.world;

/**
 * Enum representing weather types in Minecraft.
 *
 * @author ZhangYuheng
 * @since 1.0.0
 */
public enum WeatherType {

    /**
     * Clear weather (no rain, no thunder).
     */
    CLEAR,

    /**
     * Rainy weather (no thunder).
     */
    RAIN,

    /**
     * Thunderstorm weather.
     */
    THUNDER
}
